def get_all_towns(towns):
    all_towns = []
    for _, county in towns.items():
        for _, town in county.items():
            all_towns.append(town)
    return all_towns


def get_town_code(towns, county, town_name):
    if not county:
        return None
    c = towns.get(county)
    return {value: key for key, value in c.items()}.get(town_name)


def get_town_name(towns, county, town_code):
    c = towns.get(county)
    return c.get(town_code)


