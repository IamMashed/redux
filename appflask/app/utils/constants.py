class County:
    NASSAU = "nassau"
    SUFFOLK = "suffolk"
    BROWARD = "broward"
    MIAMIDADE = "miamidade"
    PALMBEACH = "palmbeach"

    _names_map = {
        NASSAU: "Nassau",
        SUFFOLK: "Suffolk",
        BROWARD: "Broward",
        MIAMIDADE: "Miami Dade",
        PALMBEACH: "Palm Beach"
    }

    _numbers_map = {
        # TODO: what is the numbers for the nassau, suffolk?!
        NASSAU: 0,
        SUFFOLK: 1,
        BROWARD: 16,
        MIAMIDADE: 23,
        PALMBEACH: 60
    }

    @classmethod
    def get_florida_counties(cls):
        return [cls.BROWARD, cls.MIAMIDADE, cls.PALMBEACH]

    @classmethod
    def get_counties(cls):
        """Get list on counties"""
        return [cls.NASSAU, cls.SUFFOLK, cls.BROWARD, cls.MIAMIDADE, cls.PALMBEACH]

    @classmethod
    def get_code(cls, name: str):
        """
        Get county code representation as we stored in database
        """
        if not name:
            return None

        name_parts = name.strip().split()

        # 'Palm Beach' or 'Miami Dade' case
        if len(name_parts) == 2:
            code = name_parts[0].lower() + name_parts[1].lower()

        # 'Nassau', 'Suffolk', 'Broward' case
        elif len(name_parts) == 1:
            code = name.lower()

        # invalid name
        else:
            return None

        # validate county code
        if code in cls.get_counties():
            return code
        else:
            return None

    @classmethod
    def names_map(cls):
        return cls._names_map

    @classmethod
    def numbers_map(cls):
        return cls._numbers_map


def extract_date_from_photo_name(photo_name):
    try:
        photo_name = photo_name.replace('.jpg', '')
        date_str = photo_name.split('_')[2]
        return datetime.strptime(date_str, "%Y-%m-%d").date()
    except IndexError:
        return None


